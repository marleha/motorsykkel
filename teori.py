# Gi korte svar på spørsmålene under:

# 1. Hva er innkapsling? Hvorfor er det nyttig?
#Innkapsling er når man skriver self._ til objektene, hver gang man bruker objektene.  
#self._ forteller brukeren at objektene er å regnes som private, eller burde anses som 
#private ovenfor programmereren.

# 2. Hva er grensesnittet til en klasse? Hvordan skiller det seg fra implementasjonen av
# en klasse?
# Et objekt tilbyr et sett av metoder, og dette er objektets grensesnitt.
# Grensesnittet bestemmes av objektets klasse og på den måten skiller grensesnitt 
# seg fra implementasjonen av en klasse. En klasse er en abstrak representasjon av en
# modell. Et grensenitt er håndtaket inn til objektet.

# 3. Hva er en instansmetode, og hvordan skiller dette seg fra prosedyrene/funksjonene
# vi har møtt hittil?
# Instansmetode har alltid self som første parameter. Der det samme som en metode, for eksempel def kjor(self,km):. En metode kan returnere en verdi, som en funksjon kan. 
# Metoder er omtrent det samme som prosedyrer og funksjoner, men skiller seg fra dem fordi de alltid tilhører et objekt eller klasse.
# Derfor bruker vi "dot-notasjon"(._). Objekt oppstår på grunn av klasse. 